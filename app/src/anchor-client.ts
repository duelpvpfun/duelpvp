/**
 * DUELPVP client — VRF (ORAO) edition, hardened.
 *
 * create_duel -> join_duel (deposit + request randomness) -> settle_duel.
 * The joiner generates fresh random `force` at join time, so no one can
 * pre-create the randomness PDA or learn the outcome in advance.
 */
import * as anchor from "@coral-xyz/anchor";
import { BN, Program } from "@coral-xyz/anchor";
import { Connection, PublicKey, SystemProgram } from "@solana/web3.js";
import { randomBytes } from "@noble/hashes/utils";
import {
  Orao,
  networkStateAccountAddress,
  randomnessAccountAddress,
} from "@orao-network/solana-vrf";
import type { Duelpvp } from "../target/types/duelpvp";

export type DuelProgram = Program<Duelpvp>;

export const TREASURY_SEED = "treasury";
export const DUEL_SEED = "duel";

// ---------------------------------------------------------------------------
// PDA derivation
// ---------------------------------------------------------------------------

export function deriveTreasuryPda(programId: PublicKey): [PublicKey, number] {
  return PublicKey.findProgramAddressSync([Buffer.from(TREASURY_SEED)], programId);
}

export function deriveDuelPda(
  gameId: BN,
  creator: PublicKey,
  programId: PublicKey
): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from(DUEL_SEED), gameId.toArrayLike(Buffer, "le", 8), creator.toBuffer()],
    programId
  );
}

export function generateGameId(): BN {
  return new BN(randomBytes(8), "le");
}

/** Fresh 32-byte VRF seed generated by the joiner. Never reuse one. */
export function generateForce(): Uint8Array {
  return randomBytes(32);
}

/** Unbiased d6 derivation (rejection sampling), mirrors the on-chain logic. */
export function deriveDice(randomness: Uint8Array): {
  creator: [number, number];
  opponent: [number, number];
} {
  const faces: number[] = [];
  for (let i = 0; i < randomness.length && faces.length < 4; i++) {
    if (randomness[i] < 252) faces.push((randomness[i] % 6) + 1);
  }
  while (faces.length < 4) faces.push((randomness[0] % 6) + 1);
  return { creator: [faces[0], faces[1]], opponent: [faces[2], faces[3]] };
}

