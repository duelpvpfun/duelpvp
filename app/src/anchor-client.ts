const BPF_LOADER_UPGRADEABLE = new PublicKey(
  "BPFLoaderUpgradeab1e11111111111111111111111"
);

export function deriveProgramDataPda(programId: PublicKey): PublicKey {
  return PublicKey.findProgramAddressSync(
    [programId.toBuffer()],
    BPF_LOADER_UPGRADEABLE
  )[0];
}

/** Run ONCE after deploy, signed by the program's upgrade authority (deployer). */
export async function initializeTreasury(program: DuelProgram, admin: PublicKey) {
  const [treasury] = deriveTreasuryPda(program.programId);
  return program.methods
    .initializeTreasury()
    .accounts({
      admin,
      treasury,
      programData: deriveProgramDataPda(program.programId),
      systemProgram: SystemProgram.programId,
    })
    .rpc();
}

export async function setPaused(program: DuelProgram, admin: PublicKey, paused: boolean) {
  const [treasury] = deriveTreasuryPda(program.programId);
  return program.methods.setPaused(paused).accounts({ admin, treasury }).rpc();
}

export async function setMaxBet(program: DuelProgram, admin: PublicKey, maxBetLamports: BN) {
  const [treasury] = deriveTreasuryPda(program.programId);
  return program.methods.setMaxBet(maxBetLamports).accounts({ admin, treasury }).rpc();
}

// ---------------------------------------------------------------------------
// Game flow
// ---------------------------------------------------------------------------

export async function createDuel(
  program: DuelProgram,
  creator: PublicKey,
  gameId: BN,
  betLamports: BN,
  winCondition: WinCondition,
  requiredOpponent: PublicKey | null,
  joinTimeoutSeconds: number | null = null
) {
  const [duel] = deriveDuelPda(gameId, creator, program.programId);
  const [treasury] = deriveTreasuryPda(program.programId);
  await program.methods
    .createDuel(
      gameId,
      betLamports,
      winCondition,
      requiredOpponent,
      joinTimeoutSeconds === null ? null : new BN(joinTimeoutSeconds)
    )
    .accounts({ creator, duel, treasury, systemProgram: SystemProgram.programId })
    .rpc();
  return { duel, gameId };
}

/**
 * Opponent joins: deposits the matching bet + requests VRF in one tx.
 * Generates fresh random `force`. Does a balance pre-flight so the tx doesn't
 * fail mid-flight if the wallet can't cover bet + VRF fee + tx fees.
 */
export async function joinDuel(
  program: DuelProgram,
  connection: Connection,
  opponent: PublicKey,
  creator: PublicKey,
  gameId: BN
) {
  const vrf = new Orao(program.provider as anchor.AnchorProvider);
  const [duel] = deriveDuelPda(gameId, creator, program.programId);
  const duelAcc = await program.account.duel.fetch(duel);

  const ns = await vrf.getNetworkState();
  const vrfFee: BN = ns.config.requestFee as BN;
  const bet: BN = duelAcc.betLamports as BN;
  const needed = bet.add(vrfFee).add(new BN(10_000_000)); // + headroom for tx/rent
  const balance = await connection.getBalance(opponent);
  if (new BN(balance).lt(needed)) {
    throw new Error(
      `Insufficient balance: need ~${needed.toString()} lamports (bet + VRF fee + fees), have ${balance}.`
    );
  }

  const force = generateForce();
  const random = randomnessAccountAddress(Buffer.from(force));

  await program.methods
    .joinDuel(gameId, Array.from(force))
    .accounts({
      opponent,
      creator,
      duel,
      vrfConfig: networkStateAccountAddress(),
      vrfTreasury: ns.config.treasury,
      random,
      vrf: vrf.programId,
      systemProgram: SystemProgram.programId,
    })
    .rpc();

  return { duel, random, force };
}

/** Wait until ORAO fulfils (the ~1-3s "rolling" window). */
export async function waitForRandomness(
  program: DuelProgram,
  random: PublicKey
): Promise<Uint8Array> {
  const vrf = new Orao(program.provider as anchor.AnchorProvider);
  const r = await vrf.waitFulfilled(random.toBuffer());
  return Uint8Array.from(r.randomness);
}

/**
 * Permissionless settle. Does NOT touch the treasury — the 1% fee stays in the
 * duel escrow and is swept to the treasury later on `close`. This keeps settles
 * fully parallel across thousands of concurrent duels (no shared write-lock).
 */
export async function settleDuel(
  program: DuelProgram,
  caller: PublicKey,
  creator: PublicKey,
  opponent: PublicKey,
  gameId: BN
) {
  const [duel] = deriveDuelPda(gameId, creator, program.programId);
  const duelAcc = await program.account.duel.fetch(duel);
  return program.methods
    .settleDuel(gameId)
    .accounts({
      caller,
      creator,
      opponent,
      duel,
      random: duelAcc.randomness as PublicKey,
    })
    .rpc();
}

/**
 * Close / refund (see program docs for the three paths). Also sweeps the
 * accrued house fee to the treasury on the Settled path. Requires the duel's
 * `randomness` account: on the Rolling (stuck-VRF) refund path the program
 * verifies it matches and is NOT yet fulfilled, so a determined-but-unsettled
 * duel can never be refunded out from under the winner.
 */
export async function closeDuel(
  program: DuelProgram,
  caller: PublicKey,
  creator: PublicKey,
  gameId: BN,
  opponent?: PublicKey
) {
  const [duel] = deriveDuelPda(gameId, creator, program.programId);
  const [treasury] = deriveTreasuryPda(program.programId);
  const duelAcc = await program.account.duel.fetch(duel);
  // duel.randomness is Pubkey::default() (system program) until someone joins;
  // that's fine — the program only enforces it on the Rolling path.
  const random = duelAcc.randomness as PublicKey;
  return program.methods
    .closeDuel(gameId)
    .accounts({
      caller,
      creator,
      opponent: opponent ?? creator,
      treasury,
      random,
      duel,
    })
    .rpc();
}

/** Creator cancels their own unmatched duel and gets refunded. */
export async function cancelUnmatchedDuel(
  program: DuelProgram,
  creator: PublicKey,
  gameId: BN
) {
  return closeDuel(program, creator, creator, gameId);
}

